namespace Task5;
public class Task5
{
        /// <summary>
        /// This method calculates the product of two numbers
        /// </summary>
        /// <param name="d">The first number</param>
        /// <param name="e">The second number</param>
        /// <returns>The product of the two numbers</returns>
        public static double product(double d, double e)
        {
            return d * e;
        }

        /// <summary>
        /// This method calculates the product of three numbers
        /// </summary>
        /// <param name="d">The first number</param>
        /// <param name="e">The second number</param>
        /// <param name="f">The third number</param>
        /// <returns>The product of the three numbers</returns>
        public static double product(double d, double e, double f)
        {
            return d * e * f;
        }
}
